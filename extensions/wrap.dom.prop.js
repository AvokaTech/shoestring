// Extensions
(function( undefined ){
	wrap.fn.prop = function( name, val ){
		name = wrap.propFix[ name ] || name;
		if( val !== undefined ){
			return this.each(function(){
				this[ name ] = val;
			});
		}
		else {
			return this[ 0 ][ name ];
		}
	};
	
	// Property normalization, a subset taken from jQuery src
	wrap.propFix = {
		"class": "className",
		contenteditable: "contentEditable",
		"for": "htmlFor",
		readonly: "readOnly",
		tabindex: "tabIndex"
	};
}());